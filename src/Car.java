public class Car {
    private String model;
    private String name;
    private int speed;

    void setModel(String model) {
        this.model = model;
    }

    void setName(String name) {
        this.name = name;
    }
    void setSpeed(int speed){
        this.speed=speed;
    }
    String getModel() {
        return model;
    }

    String getName() {
        return name;
    }
    int getSpeed(){
        return speed;
    }

}
