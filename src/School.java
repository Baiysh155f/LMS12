public class School {
    private String school;
    private String course;
    private int time;

    void setSchool(String school) {
        this.school = school;
    }

    void setCourse(String course) {
        this.course = course;
    }

    void setTime(int time){
        this.time = time;
    }
    String getSchool() {
        return school;
    }

    String getCourse() {
        return course;
    }

    int getTime(){
        return time;
    }
}
